    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String uname = username.getText().trim();
    String mail = email.getText().trim();
    String secQ = squestion.getText().trim();
    String ans = answer.getText().trim();

    if (uname.isEmpty() || mail.isEmpty() || secQ.isEmpty() || ans.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields are required!");
        return;
    }

    Bank bank = Bank.getInstance(); // Singleton or shared Bank object
    Customer customer = bank.findCustomerByUsername(uname);

    if (customer != null &&
        customer.getEmail().equals(mail) &&
        customer.getSecurityQuestion().equals(secQ) &&
        customer.getSecurityAnswer().equals(ans)) {

        JOptionPane.showMessageDialog(this, "Verification successful!");
        new ResetPassword(customer).setVisible(true);
        this.dispose();

    } else {
        JOptionPane.showMessageDialog(this, "Invalid details. Please try again.");
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

