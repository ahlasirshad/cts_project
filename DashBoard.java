    private void login_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_btnActionPerformed
    
    String uusername = username.getText();
    String ppassword = new String(password.getPassword());

    Bank bank = Bank.getInstance();   // SAME BANK

    Customer customer = bank.validateLogin(uusername, ppassword);

    if (customer != null) {
        loggedInCustomer = customer; 
        JOptionPane.showMessageDialog(this, "Login successful!");
        new Welcome(loggedInCustomer).setVisible(true);
        this.dispose();
    } else {
        JOptionPane.showMessageDialog(
            this,
            "Invalid username or password",
            "Login Error",
            JOptionPane.ERROR_MESSAGE
        );
    }
    
    }
