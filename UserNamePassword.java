    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // "Create" Button Action
    String uusername = username.getText().trim(); // Get text from username field
    String ppassword = password.getText();        // Get text from password field
    String confirmPassword = cfpassword.getText(); // Get text from confirm password field

    // Basic Validation
    if (uusername.isEmpty() || ppassword.isEmpty() || confirmPassword.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (!ppassword.equals(confirmPassword)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Passwords do not match.", "Validation Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    Customer newCustomer = new Customer(uusername, ppassword);

    // Show success message
    javax.swing.JOptionPane.showMessageDialog(this, "Account created successfully!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        
    }//GEN-LAST:event_jButton1ActionPerformed

